FString FGMPAngelScriptCodeGen::BuildScriptText()
{
	using namespace GMPAsCodeGen;
	UMessageTagsManager& Mgr = UMessageTagsManager::Get();

	FMessageTagContainer All;
	Mgr.RequestAllMessageTags(All, /*OnlyIncludeDictionaryTags*/ true);

	FString Body;
	Body += TEXT("// Auto-generated by FGMPAngelScriptCodeGen. Do not edit; regenerated from MessageTag signatures.\n");
	Body += TEXT("// Strongly-typed GMP listen/notify: compile-time checked + IntelliSense.\n\n");

	int32 Count = 0;
	for (const FMessageTag& Tag : All)
	{
		TSharedPtr<FMessageTagNode> Node = Mgr.FindTagNode(Tag);
		if (!Node.IsValid() || Node->Parameters.Num() == 0)
			continue;

		TArray<FSigParam> Params;
		if (!CollectParams(Node, Params))
			continue;

		const FString TagStr = Node->GetCompleteTagString();
		const FString Id = SanitizeIdent(TagStr);

		FString ParamDecl, ParamNames;
		for (int32 i = 0; i < Params.Num(); ++i)
		{
			if (i)
			{
				ParamDecl += TEXT(", ");
				ParamNames += TEXT(", ");
			}
			ParamDecl += FString::Printf(TEXT("%s %s"), *Params[i].AsType, *Params[i].Name);
			ParamNames += Params[i].Name;
		}

		// Declaration stubs only (no body): the implementations are registered at runtime via BindGlobalGenericFunction
		// per tag, so these give the AS compiler strong-typed checking + IntelliSense while runtime does the real work.
		Body += FString::Printf(TEXT("// %s\n"), *TagStr);
		Body += FString::Printf(TEXT("funcdef void FOn_%s(%s);\n"), *Id, *ParamDecl);
		Body += FString::Printf(TEXT("int64 Listen_%s(UObject WeakObj, FOn_%s@ cb, int Times = -1);\n"), *Id, *Id);
		Body += FString::Printf(TEXT("void Notify_%s(UObject Sender%s%s);\n\n"), *Id, Params.Num() ? TEXT(", ") : TEXT(""), *ParamDecl);
		++Count;
	}

	FString Header = FString::Printf(TEXT("// %d messages with typed signatures.\n\n"), Count);
	return Header + Body;
}

bool FGMPAngelScriptCodeGen::Generate(FString* OutText)
{
	const FString Text = BuildScriptText();
	if (OutText)
		*OutText = Text;

	const FString OutPath = FPaths::Combine(FPaths::ProjectDir(), TEXT("Script"), TEXT("GMPMessages.as"));
	FString Existing;
	if (FFileHelper::LoadFileToString(Existing, *OutPath) && Existing.Equals(Text))
		return false;

	const bool bOk = FFileHelper::SaveStringToFile(Text, *OutPath);
	UE_LOG(LogTemp, Log, TEXT("[GMPAngelScript] %s %s"), bOk ? TEXT("wrote") : TEXT("FAILED to write"), *OutPath);
	return bOk;
}

namespace GMPAsCodeGen
{
static FDelegateHandle GSigHandle;
}

void FGMPAngelScriptCodeGen::Register()
{
	if (GMPAsCodeGen::GSigHandle.IsValid())
		return;
	GMPAsCodeGen::GSigHandle = UMessageTagsManager::OnMessageTagSignatureChanged().AddLambda([](FName, bool) { FGMPAngelScriptCodeGen::Generate(); });
}

void FGMPAngelScriptCodeGen::Unregister()
{
	if (GMPAsCodeGen::GSigHandle.IsValid())
	{
		UMessageTagsManager::OnMessageTagSignatureChanged().Remove(GMPAsCodeGen::GSigHandle);
		GMPAsCodeGen::GSigHandle.Reset();
	}
}
