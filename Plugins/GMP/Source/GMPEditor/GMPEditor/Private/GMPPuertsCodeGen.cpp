// GMP type name -> TS type for the .d.ts (mirrors puerts DeclarationGenerator GenTypeDecl: number/bigint/string/boolean,
// UE.<Class>, UE.TArray<T>). Unknown -> any.
static FString TsType(const FString& GmpType)
{
	static const TMap<FString, FString> Scalars = {
		{TEXT("bool"), TEXT("boolean")},
		{TEXT("int8"), TEXT("number")}, {TEXT("uint8"), TEXT("number")}, {TEXT("int16"), TEXT("number")}, {TEXT("uint16"), TEXT("number")},
		{TEXT("int32"), TEXT("number")}, {TEXT("int"), TEXT("number")}, {TEXT("uint32"), TEXT("number")}, {TEXT("uint"), TEXT("number")},
		{TEXT("int64"), TEXT("bigint")}, {TEXT("uint64"), TEXT("bigint")}, {TEXT("float"), TEXT("number")}, {TEXT("double"), TEXT("number")},
		{TEXT("Name"), TEXT("string")}, {TEXT("Text"), TEXT("string")}, {TEXT("String"), TEXT("string")},
	};
	bool bK = true;
	return MapType(GmpType, Scalars, TEXT("any"),
		[](UClass* C) { return TEXT("UE.") + C->GetName(); },
		[](UScriptStruct* S) { return TEXT("UE.") + S->GetName(); },
		bK);
}

// GMP type name -> C++ spelling for the SB locals (Converter<T>::toCpp / typed slots), same table as slua/UnLua.
static FString CppType(const FString& GmpType, bool& bOutKnown)
{
	static const TMap<FString, FString> Scalars = {
		{TEXT("bool"), TEXT("bool")}, {TEXT("int8"), TEXT("int8")}, {TEXT("uint8"), TEXT("uint8")},
		{TEXT("int16"), TEXT("int16")}, {TEXT("uint16"), TEXT("uint16")},
		{TEXT("int32"), TEXT("int32")}, {TEXT("int"), TEXT("int32")}, {TEXT("uint32"), TEXT("uint32")}, {TEXT("uint"), TEXT("uint32")},
		{TEXT("int64"), TEXT("int64")}, {TEXT("uint64"), TEXT("uint64")}, {TEXT("float"), TEXT("float")}, {TEXT("double"), TEXT("double")},
		{TEXT("Name"), TEXT("FName")}, {TEXT("Text"), TEXT("FText")}, {TEXT("String"), TEXT("FString")},
		{TEXT("Object"), TEXT("UObject*")}, {TEXT("Class"), TEXT("UClass*")},
	};
	return MapType(GmpType, Scalars, TEXT("UObject*"),
		[](UClass* C) { return C->GetPrefixCPP() + C->GetName() + TEXT("*"); },
		[](UScriptStruct* S) { return FString(S->GetPrefixCPP()) + S->GetName(); },
		bOutKnown);
}
