static FString SanitizeIdent(const FString& In)
{
	FString Out = In;
	for (TCHAR& C : Out)
	{
		const bool bOk = (C >= '0' && C <= '9') || (C >= 'A' && C <= 'Z') || (C >= 'a' && C <= 'z') || C == '_';
		if (!bOk)
			C = '_';
	}
	return Out;
}

// Resolve a bare UE type name to its AngelScript spelling via reflection (class -> A/U prefix, struct -> F prefix); unknown -> UObject.
static FString AsReflectedName(const FString& BareName, bool& bOutKnown)
{
	bOutKnown = true;
	if (UClass* Cls = UClass::TryFindTypeSlow<UClass>(BareName))
		return Cls->GetPrefixCPP() + Cls->GetName();
	if (UScriptStruct* Struct = UClass::TryFindTypeSlow<UScriptStruct>(BareName))
		return FString(Struct->GetPrefixCPP()) + Struct->GetName();
	bOutKnown = false;
	return TEXT("UObject");
}

// GMP type name (FName) -> AngelScript type. Scalars map directly; containers recurse; UObject/struct via reflection.
static FString MapType(const FString& GmpType, bool& bOutKnown)
{
	bOutKnown = true;
	static const TMap<FString, FString> Scalars = {
		{TEXT("bool"), TEXT("bool")},
		{TEXT("int8"), TEXT("int8")}, {TEXT("uint8"), TEXT("uint8")},
		{TEXT("int16"), TEXT("int16")}, {TEXT("uint16"), TEXT("uint16")},
		{TEXT("int32"), TEXT("int")}, {TEXT("int"), TEXT("int")}, {TEXT("uint32"), TEXT("uint")}, {TEXT("uint"), TEXT("uint")},
		{TEXT("int64"), TEXT("int64")}, {TEXT("uint64"), TEXT("uint64")},
		{TEXT("float"), TEXT("float")}, {TEXT("double"), TEXT("double")},
		{TEXT("Name"), TEXT("FName")}, {TEXT("Text"), TEXT("FText")}, {TEXT("String"), TEXT("FString")},
		{TEXT("Object"), TEXT("UObject")}, {TEXT("Class"), TEXT("UClass")},
	};
	if (const FString* Found = Scalars.Find(GmpType))
		return *Found;

	auto Inner = [&](const TCHAR* Prefix) -> FString {
		const int32 PrefixLen = FCString::Strlen(Prefix);
		return GmpType.Mid(PrefixLen, GmpType.Len() - PrefixLen - 1);
	};

	if (GmpType.StartsWith(TEXT("TArray<")) && GmpType.EndsWith(TEXT(">")))
		return FString::Printf(TEXT("TArray<%s>"), *MapType(Inner(TEXT("TArray<")), bOutKnown));
	if (GmpType.StartsWith(TEXT("TSubclassOf<")) && GmpType.EndsWith(TEXT(">")))
		return FString::Printf(TEXT("TSubclassOf<%s>"), *AsReflectedName(Inner(TEXT("TSubclassOf<")), bOutKnown));

	return AsReflectedName(GmpType, bOutKnown);
}

struct FSigParam
{
	FString AsType;
	FString Name;
};

// Collects a tag's parameter list mapped to AngelScript types. Returns false if any type is unmapped (skip the tag).
static bool CollectParams(const TSharedPtr<FMessageTagNode>& Node, TArray<FSigParam>& Out)
{
	int32 Idx = 0;
	for (const FMessageParameter& P : Node->Parameters)
	{
		bool bKnown = true;
		FString AsType = MapType(P.Type.ToString(), bKnown);
		if (!bKnown)
		{
			UE_LOG(LogTemp, Warning, TEXT("[GMPAngelScript] skip tag %s: unmapped param type '%s'"), *Node->GetCompleteTagString(), *P.Type.ToString());
			return false;
		}
		FString Name = P.Name.IsNone() ? FString::Printf(TEXT("Arg%d"), Idx) : SanitizeIdent(P.Name.ToString());
		Out.Add({MoveTemp(AsType), MoveTemp(Name)});
		++Idx;
	}
	return true;
}
